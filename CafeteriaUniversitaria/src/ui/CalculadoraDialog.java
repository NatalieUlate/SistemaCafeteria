    private void btnSignoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSignoActionPerformed
        try {
            String textoActual = txtDisplay.getText();
            double numeroActual = Double.parseDouble(textoActual);
            if (numeroActual != 0) {
                double numeroInvertido = numeroActual * -1;
                if (numeroInvertido == (long) numeroInvertido) {
                    txtDisplay.setText(String.format("%d", (long) numeroInvertido));
                } else {
                    txtDisplay.setText(String.valueOf(numeroInvertido));
                }
            }
        } catch (NumberFormatException e) {
        }
    }//GEN-LAST:event_btnSignoActionPerformed
    private void agregarDigito(String digito) {
        if (esperandoNuevoNumero) {
            txtDisplay.setText("");
            esperandoNuevoNumero = false;
        }
        if (txtDisplay.getText().equals("0") && !digito.equals(".")) {
            txtDisplay.setText(digito);
        } else {
            txtDisplay.setText(txtDisplay.getText() + digito);
        }
    }

    /**
     * Almacena el primer n√∫mero y el operador seleccionado.
     */
    private void seleccionarOperador(String operador) {
        primerNumero = Double.parseDouble(txtDisplay.getText());
        operadorActual = operador;
        esperandoNuevoNumero = true;
    }
